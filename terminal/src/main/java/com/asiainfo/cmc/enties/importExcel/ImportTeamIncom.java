package com.asiainfo.cmc.enties.importExcel;

/**
 * 售后团队收入 实体类
 * @author yuxuandong
 *
 */
public class ImportTeamIncom {

	private String month; //月份
	
	private String city;//地市
	
	private String type;//售后团队收入类型
	
	private String value;//金额

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public ImportTeamIncom(String month, String city, String type, String value) {
		super();
		this.month = month;
		this.city = city;
		this.type = type;
		this.value = value;
	}

	public ImportTeamIncom() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	
}
