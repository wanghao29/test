package com.asiainfo.cmc.enties.model.eightCharacters;

/**
 * 数据确认日志表 ZD_8TYPE_INFO_CONFIRM_LOG
 * @author yuxuandong
 *
 */
public class Zd8typeInfoConfirmLog {
	
	private int id;
	private String kpiId;//指标ID
	private String person;//确认人
	private String confirmTime;//确认时间 yyyyMMdd HHmmss
	private String month;//账期   yyyyMM
	private String state;// 0：已确认；1：请核实；
	private String remarks; //备注
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getKpiId() {
		return kpiId;
	}
	public void setKpiId(String kpiId) {
		this.kpiId = kpiId;
	}
	public String getPerson() {
		return person;
	}
	public void setPerson(String person) {
		this.person = person;
	}
	public String getConfirmTime() {
		return confirmTime;
	}
	public void setConfirmTime(String confirmTime) {
		this.confirmTime = confirmTime;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	 
}
