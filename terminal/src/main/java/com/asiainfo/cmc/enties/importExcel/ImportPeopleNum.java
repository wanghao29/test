package com.asiainfo.cmc.enties.importExcel;

/**
 * 导入出小人数
 * @author yuxuandong
 *
 */
public class ImportPeopleNum {
	
	private String type8;//'8字类型'
	
	private String dimension;//维度
	
	private String month;//'月份month 例如：201806'
	
	private String AREANAME;//'地市city'
	
	private int people_num;//促销人员数

	public String getType8() {
		return type8;
	}

	public void setType8(String type8) {
		this.type8 = type8;
	}

	public String getDimension() {
		return dimension;
	}

	public void setDimension(String dimension) {
		this.dimension = dimension;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getAREANAME() {
		return AREANAME;
	}

	public void setAREANAME(String aREANAME) {
		AREANAME = aREANAME;
	}

	public int getPeople_num() {
		return people_num;
	}

	public void setPeople_num(int people_num) {
		this.people_num = people_num;
	}

	public ImportPeopleNum(String type8, String dimension, String month, String aREANAME, int people_num) {
		super();
		this.type8 = type8;
		this.dimension = dimension;
		this.month = month;
		AREANAME = aREANAME;
		this.people_num = people_num;
	}

	public ImportPeopleNum() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	
}
